#Argumentos y parámetros

def restar(num1,num2): # Parámetros
    return num1-num2

print(restar(4,3)) #El valor de 4 se está poniendo en número 1 y viceversa  #ARGUMENTOS
#los argumentos se colocan por posición en los parámetros
#Se están pasando argumentos por posición
print(restar(num1=2,num2=4)) # Los argumentos se pueden pasar por posición
'''Argumentos se envían a la función'''