#Condicionales
'''Los condicionales son las estructuras de control
más importantes en la programación
Sirven para comparar dos valores, y va a dar como resultado un valor
lógico'''

numero= int(input("Digite un número: "))
if numero>0:
    print("El número es positivo")
elif numero==0:
    print("El número es 0")
else:
    print("El número es negativo")

print("Fin del programa")



